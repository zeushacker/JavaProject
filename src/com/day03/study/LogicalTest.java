package com.day03.study;

public class LogicalTest {

	public static void main(String[] args) {
		
		
		boolean a = true, b = false;
		
		// and 연산자(논리곱)
		System.out.printf(" a && b : %b\n", a && b);
		//or 연산자 (논리합)
		System.out.printf(" a || b : %b\n", a || b);
		// not(논리부정)
		System.out.printf(" !a : %b\n", !a);
		
		
		

	}

}
