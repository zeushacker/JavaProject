package com.day08.study;

public class Test001 {

	public static void main(String[] args) {
		
		
		int a = 10;
		int res = 0;
		
		// 복합 대입 연산자
		 res += a;  System.out.println("res ="+res);
		 res *= a;  System.out.println("res ="+res);
		 res -= a;  System.out.println("res ="+res);
		 res %= a;  System.out.println("res ="+res);
		
		
	}

}
